n=int(input())
li=[]
for i in range(n):
    l=list(map(int,input().split()))
    l[0]
    l[1]
    l[2]
    if max(l[0],l[1],l[2])==l[0]:

        li.append('Alice')
    if max(l[0],l[1],l[2])==l[1]:

        li.append('Bob')        
    if max(l[0],l[1],l[2])==l[2]:

        li.append('Charlie')

for i in li:
    print(i)
